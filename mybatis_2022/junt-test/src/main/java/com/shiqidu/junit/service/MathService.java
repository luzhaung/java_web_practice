public class MathService {
    public static void main(String[] args) {

    }

    /**
     * 加法运算
     * @param a
     * @param b
     * @return
     */
    public int sum(int a, int b) {
        return a + b;
    }

    /**
     * 减法运算
     * @param a
     * @param b
     * @return
     */
    public int sub(int a, int b) {
        return a - b;
    }
}
